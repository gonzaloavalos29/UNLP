ORG 1000H
VEC DB 5,1,4,3,1,12,20,5,1,6
CANT_PARES DB ?

ORG 3000H
ESPAR: AND AH, 01H
  JZ PAR
  MOV CL, 00H
  JMP FIN
PAR: MOV CL, 0FFH
FIN: RET

ORG 2500
CONTAR_PARES: MOV DL, 0
LOOP: CMP CH, 0
  JZ FIN2
  MOV AX, [BX]
  CALL ESPAR
  CMP CL, 0FFH ; si es par
  JNZ SEGUIR 
  INC DL ; contar
SEGUIR: INC BX
  DEC CH
  JMP LOOP
FIN2: RET

ORG 2000H
MOV BX, OFFSET VEC ; dir. vec
MOV CH, 10 ; cant. elementos
CALL CONTAR_PARES
MOV CANT_PARES, DL ; RESULTADO
HLT
END
  