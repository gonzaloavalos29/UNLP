; PROGRAMA QUE SALUDA
EOI EQU 20H
IMR EQU 21H
INT0 EQU 24H

ORG 1000H
MSJ  DB  "HOLA"

ORG 24  

ORG 3000H
SALUDAR:  MOV BX, OFFSET MSJ
          MOV AL, 4
          INT 7
          MOV AL, 20H
          OUT EOI, AL
          IRET

ORG 96 ; VECTOR DE INTERRUPCIONES
DIR_SALUDAR  DW  SALUDAR

ORG 2000H
CLI
MOV AL, 0FEH ; 1111 1110
OUT IMR, AL
MOV AL, 24
OUT INT0, AL
MOV BX, 96
MOV WORD PTR [BX], 3000H
STI
LOOP: JMP LOOP

INT 0
END