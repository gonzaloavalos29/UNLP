; INTERRUPCIÓN POR HARDWARE: TECLA F10
; CONTADOR DE VECES QUE SE TOCÓ LA TECLA F10, ACUMULO EN REGISTRO DX

EOI EQU 20H
IMR EQU 21H
INT0 EQU 24H
N_F10 EQU 10

ORG 40
IP_F10 DW RUT_F10

ORG 3000H
RUT_F10: PUSH AX
INC DX
MOV AL, 20H
OUT EOI, AL
POP AX
IRET

ORG 2000H
CLI
MOV AL, 0FEH
OUT IMR, AL
MOV AL, N_F10
OUT INT0, AL
MOV DX, 0
STI
LAZO: JMP LAZO

END